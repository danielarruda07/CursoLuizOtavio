//NaN - Not a number
const num1 = 10
const num2 = '5'
console.log(num1 + num2)
console.log(typeof num2)

//Como converter string em number usando o parseInt
const num3 = 10
const num4 = parseInt('5')
console.log(num3 + num4)
console.log(typeof num4)


//Usando o parseFloat para converter string em numero com casas decimais
const num5 = 10
const num6 = parseFloat('5.2')
console.log(num5 + num6)


//Usando number para converter string em numeros
const num7 = 8
const num8 = Number('3.21')
console.log(num7 * num8)
console.log(typeof num8)